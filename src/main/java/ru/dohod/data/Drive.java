public class Drive {
    private String description;
    private String logicalname;
    private String serial;

    public String getSerial() {
        return serial;
    }

    public String getProduct() {
        return product;
    }

    private String product;

    public String getDescription() {
        return description;
    }

    public String getLogicalname() {
        return logicalname;
    }
}
